PHONE VARCHAR2(100) NOT NULL,
EMAIL VARCHAR2(100) UNIQUE NOT NULL,
ADDR VARCHAR2(100) NOT NULL,
ZONECODE VARCHAR2(100) NOT NULL,
ENABLEDDB VARCHAR2(100) NOT NULL,
ROLE VARCHAR2(100) NOT NULL,
JOINDATE DATE NOT NULL,
