CONSTRAINT CON_PERPAY CHECK (PERPAY IN ('Y','N')),
CONSTRAINT CON_ENABLED CHECK (ENABLEDDB IN ('Y','N')),
CONSTRAINT CON_ROLE CHECK (ROLE IN ('ROLE_ADMIN','ROLE_USER'))
);
